def sumOfTwoNumbers(a: float, b: float) -> float:
    result: float = None

    result = a + b

    return result

def subOfTwoNumbers(a: float, b: float) -> float:
    result: float = None

    result = a - b

    return result

def multOfTwoNumbers(a: float, b: float) -> float:
    result: float = None

    result = a * b

    return result


def divOfTwoNumbers(a: float, b: float) -> float:
    result: float = None

    result = a / b

    return result

